#!/bin/bash

echo "Changing theme..."

# ----------------------------------------------------- 
# Update Wallpaper with pywal
# ----------------------------------------------------- 
feh --bg-fill $(shuf -n 1 -e ~/pics/wallpapers/*)

# ----------------------------------------------------- 
# Wait for 1 sec
# ----------------------------------------------------- 
sleep 1

# ----------------------------------------------------- 
# Send notification
# ----------------------------------------------------- 
notify-send "Wallpaper updated!"

echo "Done."
